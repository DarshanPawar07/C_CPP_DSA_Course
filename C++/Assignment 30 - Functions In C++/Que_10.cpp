
     /* Que -  Write functions using function overloading to add two numbers having different data types  */


     #include<iostream>
     using namespace std;

     
      int add(int a,int b)
                           {
                               return a+b;
                           } 
    
     int add(float a,float b)
                           {
                                return a+b;
                           }
     int add(double a,double b)
                           {
                                return a+b;
                           }