
        /* Que - Write a program to print the first N odd natural numbers in reverse order */

        #include<stdio.h>
        int main()

                  {

                        int n ;

                        printf("Enter The value of n\n") ;
                        scanf(" %d ", &n) ;

                        while(n)
                                  {
                                     printf(" %d\n ", 2*n ) ;
                                     n-- ;

                                  }

                        printf("\n") ;

                        return 0 ;

                  }



