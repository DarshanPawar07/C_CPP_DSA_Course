
          /* Que - Write a program to print squares of the first N natural numbers  */

          #include<stdio.h>
          int main()

                 {

                     int n , i = 1 ;

                     printf("Enter The value of n\n") ;
                     scanf(" %d ", &n) ;

                     while( i <= n )
                                    {
                                         printf(" %d\n " , i*i ) ;
                                         i++ ;
                                    }

                     return 0;


                }



